local function week_header()
  local week = week_ascii_text()
  local daysoftheweek = { "Sunday", "Monday", "Tuesday", "Wednesday", "Thursday", "Friday", "Saturday" }
  local day = daysoftheweek[os.date("*t").wday]
  local tbl = week[day]
  tbl = tbl .. "\n" .. os.date("%Y-%m-%d %H:%M:%S ")
  return tbl
end

return {
  "goolord/alpha-nvim",
  event = "VimEnter",
  enabled = true,
  init = false,
  opts = function()
    local dashboard = require("alpha.themes.dashboard")
    local logo = week_header()
    dashboard.section.header.val = vim.split(logo, "\n")
    -- stylua: ignore
    dashboard.section.buttons.val = {
      dashboard.button("f", " " .. " Find file",       "lua LazyVim.pick()()"),
      dashboard.button("n", " " .. " New file",        [[<cmd> ene <BAR> startinsert <cr>]]),
      dashboard.button("r", " " .. " Recent files",    "lua LazyVim.pick('oldfiles')()"),
      dashboard.button("g", " " .. " Find text",       'lua LazyVim.pick("live_grep")()'),
      dashboard.button("c", " " .. " Config",          "LazyVim.pick.config_files()()"),
